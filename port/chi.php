<h2 style="font-size:16px; color:#999999">
To support a better pregnancy experience, we present Nuwa, a system that enhances self-awareness of health condition and also facilitates the communication between expectant mothers and their partners. The system is based on expectant mothers' health conditions. It provides a way for expectant parents to track and understand the maternal health, and encourages the partners to provide better support. Our study results support our design ideas and argue for the feasibility and expandability of Nuwa in helping expectant parents to have better experiences during pregnancy.
</h2>


<img src="../img/CHI/000012.jpg"  class="projectbigpic"/>

<h2 style="font-size:16px; color:#999999">
We propose a mobile-based system, Nuwa, which aims to enhance expectant mothers' awareness of their health conditions and their partners' participation. Nuwa consists of two separate components: a wristband with sensors and a mobile application. The expectant mother will wear the wristband while both of the couple need to install the mobile application on their smartphones. Maternal health measurements are collected by wristband and transferred to the mobile application. Nuwa explains the measurements in plain language and generates notifications for both expectant parents in a timely manner. The system utilizes the interpretation of measurements to facilitate the collaboration between expectant parents.
</h2>

