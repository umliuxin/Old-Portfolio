<div class="projectpictitle"><h2 style="font-size:16px; color:#999999">In order to better serve the students in collecting, organizing, preserving, communicating, and sharing the various collections and programs available to patrons of the library system, we propose Rhea, a mobile application that aims to help library users to better manage their UM library issues. This application is designed for the active users in library, especially the students who often miss the due date and suffer from the penalty from library due to their heavy workload. It mainly functions as a reminder to remind the users of their return date, overdue fines, waitlist, reservations, ordering and renewing. In addition, it also collects the basic functions of library information system, such as displaying general information of the borrowed materials and related loan policy.</h2></div>


<img src="../img/project/ide-team.png"  class="projectbigpic"/>

<div class="projectpictitle"><h2 style="font-size:16px; color:#999999">Research

We started our design research with initial exploratory research to understand the desires of UM library and studentsâ€™ needs and behavior regarding the reminding system of UM library.We conducted interviews with both undergraduate students and library staff.
 
In the interviews with students, we focused on their experience of borrowing, making reservations, getting notice, getting penalty and returning items from UM libraries. In the interviews with UM library staff, we included the questions about the book borrow policies, library administrations, online systems, and notification functions. Based on the analysis of the interviews, we gained several insights about students behavior and needs which, in turn, make us to make several design choices.
 
Based on the research, we concluded several key findings that exist in the process of borrowing, reserving and ordering items from UM library. Students sometimes are heavily fined for certain type of overdue materials (such as DVDs and textbooks). But UM library notice system sometimes fail to inform students of the status of the items they borrowed, reserved or ordered, which may cause students to overdue or get confused. Besides, the reminder service for waiting list of ordering service is not sufficient either so that students could not see the information of their activity in UM Library (borrowing, making reservation, and ordering) at the same time. 
</h2></div>

<img src="../img/project/ide-int.png"  class="projectbigpic"/>

<div class="projectpictitle"><h2 style="font-size:16px; color:#999999">Given our research findings, we pursued a primarily mobile-based system that we have named Rhea. The system would allow users to check the status of their borrowed, reserved and ordered items, and it would also remind students of those status changes. Rhea focuses on three kinds of user behaviors: borrowing, waiting and ordering, and it mainly has three functions: Reminder, fine calculator, and order manager. Moreover, Rhea enables users to check out the basic information of the library resources in a convergent and consistent way. 
