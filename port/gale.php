<div class="projectpictitle"><h2 style="font-size:16px; color:#999999">
We interviewed three User Experience Specialists and the User Experience Specialist Manager, a systems analyst, a delivery lead, a managing editor, and a production manager.  After each interview we spent at least two hours in interpretation sessions, discussing the data collected from the interviews and observations of their work habits and environment. Then we would break the information down into different work models; artifact, communication flow, cultural, physical, sequence, and swimlane. Consolidating the work models from across all the different positions we interviewed was very helpful and gave us great insight into the corporate culture and the flow of information between departments.


</h2></div>

